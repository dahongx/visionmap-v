/**
 * 递归生成 simple-mind-map 的节点数据
 */
function treeToMindMapData(node) {
  return {
    data: {
      text: node.text,
      richText: true,
      expand: true,
    },
    children: (node.children || []).map(treeToMindMapData),
  };
}

