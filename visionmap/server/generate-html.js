/**
 * 主函数：生成 HTML 文件
 * @param {string} title - 标题
 * @param {string} mapType - 导图类型
 * @param {object} jsonData - simple-mind-map 格式的 JSON 数据
 * @param {string} outputDir - 输出目录（用于复制 lib 文件）
 * @returns {string} HTML 内容
 */
export async function generateHtml(title, mapType, jsonData, outputDir) {
  // 优先生成完整交互版（使用 simple-mind-map）
  if (jsonData && jsonData.root) {
    // 复制 lib 文件到输出目录
    const outDir = outputDir || path.join(process.cwd(), "output");
    const libDir = path.join(outDir, "lib");
    await fs.mkdir(libDir, { recursive: true });
    const libTarget = path.join(libDir, "simpleMindMap.umd.min.js");
    try {
      await fs.copyFile(LIB_SOURCE, libTarget);
    } catch (e) {
      console.error("复制 lib 文件失败:", e.message);
      console.error("源文件:", LIB_SOURCE);
      console.error("目标:", libTarget);
    }
    return generateFullHtml(title, mapType, jsonData);
  }

  // 降级到 Mermaid 版（轻量预览）
  const { generateMermaid } = await import("./generate-mermaid.js");
  const mermaidCode = await generateMermaid(title, mapType);
  return generateMermaidHtml(title, mapType, mermaidCode);
}
